/*
* 변수의 사용
* 1. var : 더 이상 사용하지 않는다.
* 2. let : 일반 변수의 선언
* 3. const : 상수의 선언
*/

var name = '장원영';
console.log(name);

let ive = '아이브';
console.log(ive)

ive = '안유진';
console.log(ive);

let hong = null;
console.log(hong);

// 상수의 선언
const newJeans = '뉴진스';
console.log(newJeans);

// 상수의 값 변경
newJeans = '뉴진스님';
console.log(newJeans)